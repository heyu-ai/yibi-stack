def _test_for_hookify_rule(proposal: ArtifactProposal) -> str:
    """Generate pytest for a new Python PreToolUse hook.

    Failing condition (before): hook file does not exist, so the check never runs.
    Passing condition (after): hook file exists and blocks the problematic pattern.
    """
    hook_path = proposal.target_file
    bad_cmd = _extract_bad_command(proposal)
    # Use repr() to get a properly-escaped Python string literal (handles \n, quotes, etc.)
    bad_cmd_repr = repr(bad_cmd)

    return textwrap.dedent(f'''\
        """
        NIGHTLY-{proposal.id[:8]} — generated by nightly-agent
        Validates: {proposal.title}
        Artifact: {hook_path}

        Failing test: hook file does not exist → friction undetected.
        Passing test: hook file exists and blocks the pattern.
        """
        import json
        import subprocess
        from pathlib import Path

        import pytest

        # Resolve repo root at test execution time (not generation time)
        _root = Path(
            subprocess.check_output(
                ["git", "rev-parse", "--show-toplevel"], text=True
            ).strip()
        )
        HOOK_PATH = _root / "{hook_path}"
        BAD_CMD = {bad_cmd_repr}


        def test_hook_file_exists():
            """Rule file must be present before this test can pass."""
            assert HOOK_PATH.exists(), (
                f"Hook not found: {{HOOK_PATH}}\\n"
                "The nightly-agent PR must create this file."
            )


        def test_hook_blocks_bad_command():
            """Hook must exit non-zero for the problematic command."""
            assert HOOK_PATH.exists(), "skip — hook not yet created"
            hook_input = {{
                "hook_event_name": "PreToolUse",
                "tool_name": "Bash",
                "tool_input": {{"command": BAD_CMD}},
            }}
            result = subprocess.run(  # nosec B603
                ["python3", str(HOOK_PATH)],
                input=json.dumps(hook_input),
                text=True,
                capture_output=True,
                timeout=10,
            )
            assert result.returncode != 0, (
                f"Hook should have blocked:\\n  {{BAD_CMD}}\\n"
                f"stdout: {{result.stdout[:200]}}\\nstderr: {{result.stderr[:200]}}"
            )


        def test_hook_allows_good_command():
            """Hook must exit 0 for a clean command."""
            if not HOOK_PATH.exists():
                pytest.skip("hook not yet created")
            good_input = {{
                "hook_event_name": "PreToolUse",
                "tool_name": "Bash",
                "tool_input": {{"command": 'echo "clean command"'}},
            }}
            result = subprocess.run(  # nosec B603
                ["python3", str(HOOK_PATH)],
                input=json.dumps(good_input),
                text=True,
                capture_output=True,
                timeout=10,
            )
            assert result.returncode == 0, (
                f"Hook should have allowed clean command\\n"
                f"stderr: {{result.stderr[:200]}}"
            )
    ''')


def _test_for_claude_md_gotcha(proposal: ArtifactProposal) -> str:
    """Generate pytest for a CLAUDE.md gotcha addition.

    Failing condition (before): the gotcha text is not in CLAUDE.md.
    Passing condition (after): PR has added the text.
    """
    phrase = _extract_distinctive_phrase(proposal.content)
    phrase_repr = repr(phrase)
    target = proposal.target_file

    return textwrap.dedent(f'''\
        """
        NIGHTLY-{proposal.id[:8]} — generated by nightly-agent
        Validates: {proposal.title}
        Artifact: {target}

        Failing test: gotcha text not yet present in target file.
        Passing test: PR has added it.
        """
        import subprocess
        from pathlib import Path

        _root = Path(
            subprocess.check_output(
                ["git", "rev-parse", "--show-toplevel"], text=True
            ).strip()
        )
        TARGET_FILE = _root / "{target}"
        DISTINCTIVE_PHRASE = {phrase_repr}


        def test_gotcha_present_in_target():
            """Gotcha must appear in the target file after the PR lands."""
            assert TARGET_FILE.exists(), f"Target file not found: {{TARGET_FILE}}"
            content = TARGET_FILE.read_text(encoding="utf-8")
            assert DISTINCTIVE_PHRASE.lower() in content.lower(), (
                f"Gotcha not found in {{TARGET_FILE}}\\n"
                f"Expected phrase: {{DISTINCTIVE_PHRASE!r}}\\n"
                "The nightly-agent PR must add this gotcha."
            )
    ''')


