_TEST_GENERATORS = {
    ArtifactType.HOOKIFY_RULE: _test_for_hookify_rule,
    ArtifactType.CLAUDE_MD_GOTCHA: _test_for_claude_md_gotcha,
    ArtifactType.SKILL_UPDATE: _test_for_skill_update,
}

